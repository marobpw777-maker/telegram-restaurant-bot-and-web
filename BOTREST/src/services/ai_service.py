def detect_intent(text: str) -> str:
    """
    Определяет намерение пользователя по тексту.
    Возвращает: 'sommelier', 'concierge', или 'unknown'
    """
    text_lower = text.lower().strip()

    sommelier_score = sum(1 for p in SOMMELIER_TRIGGERS if re.search(p, text_lower))
    concierge_score = sum(1 for p in CONCIERGE_TRIGGERS if re.search(p, text_lower))

    if sommelier_score > concierge_score:
        return "sommelier"
    elif concierge_score > sommelier_score:
        return "concierge"
    return "unknown"


def wants_sommelier(text: str) -> bool:
    """Гость явно просит сомелье."""
    patterns = [
        r"(?:хочу|дай|позови|можно|вызови|дайте|позовите)\s*(?:мне\s*)?сомелье",
        r"(?:хочу|можно)\s+поговорить\s+(?:с\s+)?сомелье",
        r"переключи(?:те)?\s+(?:на\s+)?сомелье",
    ]
    return any(re.search(p, text.lower()) for p in patterns)


def wants_concierge(text: str) -> bool:
    """Гость явно просит консьержа."""
    patterns = [
        r"(?:хочу|дай|позови|можно|вызови|дайте|позовите)\s*(?:мне\s*)?консьерж",
        r"(?:хочу|можно)\s+поговорить\s+(?:с\s+)?консьерж",
        r"переключи(?:те)?\s+(?:на\s+)?консьерж",
    ]
    return any(re.search(p, text.lower()) for p in patterns)


